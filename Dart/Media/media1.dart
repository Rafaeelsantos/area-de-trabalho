void main() {

  var prova1 = 10;
  var prova2 = 5;
  var media = (prova1 + prova2) / 2;

  print(media);

  //Printa se a media é maior ou igual ao valor!
  print(media >= 7);

  //Printa se a media é menor que o valor!
  print(media < 7);

  //Printa se os dois valores são iguais!
  print(prova1 == prova2);

  //Printa se os dois valores são diferentes!
  print(prova1 != prova2);

  //Printa se os dois valores são maiores que 7!
  print(prova1 >= 7 && prova2 >= 7);

  //Printa se algum dos valores é igual a 10!
  print(prova1 == 10 || prova2 == 10);

}